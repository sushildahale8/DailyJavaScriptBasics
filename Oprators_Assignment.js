/*
Assignment Operator
1. = Equal to
2. += Plus Equal to
3. -= Minus Equal to
4. *= Multiplication equal to
5. /= Divisoin equal to
6. %= Modulus equal to
*/

// 1. = Equal to

let a = 10;
let b = 20;
let sum = a+b;
console.log("Equal = ",sum);

// 2. += Plus Equal to

let x = 20;
let y = 30;
console.log(x += y); // x = x + y

// 3. -= Minus Equal to

let p = 10;
let q = 20;
console.log(p -= y); // p = p - q

// 4. *= Multiplication equal to

let r = 10;
let s = 20;
console.log(r *= s); // r = r * s

//5. /= Divisoin equal to

let c = 30;
let d = 25;
console.log(c/=d); // c= c/d

//6. %= Modulus equal to

let i = 4;
let j = 2;
console.log(i%=j);
