let a = 10;
let b = 20 ;

// 1. Addition +
console.log("Addition", a+b);

// 2. Subtraction -
console.log("Subtraction", a-b);

// 3. Multiplication *
console.log("Multiplication", a*b);

// 4. Divison /
console.log("Divison", a/b);

// 5. Modulus %
console.log("Modulus",a%b);

// 6. Incremental ++
console.log("Value of 'A' before incerment", a);
a++;
console.log("Value of 'A' after incerment", a);

// 7. Decremental --
console.log("Value of 'B' before Decrement", b);
b--;
console.log("Value of 'B' after Decrement", b);